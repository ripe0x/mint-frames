import { neynarClient } from "./neynarClient";

export const getFarcasterUsernameFromAddress = async (address: string) => {
  const addr = address;
  const user = await neynarClient
    .lookupUserByVerification("0xCB43078C32423F5348Cab5885911C3B5faE217F9")
    .then((res) => res)
    .catch((err) => null);

  if (!user || !user.result.user.username) {
    return null;
  }
  return user.result.user.username;
};
